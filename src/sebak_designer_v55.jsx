// 간이 마크다운 렌더러 (헤딩/볼드/표/리스트만)
// ============================================================
function MarkdownRenderer({ content }) {
  if (!content) return null;
  const lines = content.split("\n");
  const elements = [];
  let tableBuffer = [];
  let listBuffer = [];

  function flushTable() {
    if (tableBuffer.length === 0) return;
    const rows = tableBuffer.map(line => line.split("|").slice(1, -1).map(c => c.trim()));
    const header = rows[0];
    const body = rows.slice(2);
    elements.push(
      <table key={`tbl-${elements.length}`} style={{ width: "100%", borderCollapse: "collapse", margin: "16px 0", fontSize: "13px" }}>
        <thead>
          <tr style={{ background: "#fafaf7" }}>
            {header.map((h, i) => (
              <th key={i} style={{ padding: "10px", textAlign: "left", borderBottom: "2px solid #1a1a1a", fontWeight: 600 }}>{h}</th>
            ))}
          </tr>
        </thead>
        <tbody>
          {body.map((row, ri) => (
            <tr key={ri}>
              {row.map((cell, ci) => (
                <td key={ci} style={{ padding: "10px", borderBottom: "1px solid #eee", verticalAlign: "top" }}>
                  {renderInline(cell)}
                </td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
    );
    tableBuffer = [];
  }

  function flushList() {
    if (listBuffer.length === 0) return;
    elements.push(
      <ul key={`list-${elements.length}`} style={{ margin: "8px 0", paddingLeft: "20px", lineHeight: 1.8 }}>
        {listBuffer.map((item, i) => (
          <li key={i} style={{ fontSize: "14px", color: "#333", marginBottom: "4px" }}>{renderInline(item)}</li>
        ))}
      </ul>
    );
    listBuffer = [];
  }

  function renderInline(text) {
    const parts = [];
    let remaining = text;
    let key = 0;
    while (remaining.length > 0) {
      const boldMatch = remaining.match(/\*\*(.+?)\*\*/);
      if (boldMatch) {
        parts.push(remaining.slice(0, boldMatch.index));
        parts.push(<strong key={key++} style={{ fontWeight: 600 }}>{boldMatch[1]}</strong>);
        remaining = remaining.slice(boldMatch.index + boldMatch[0].length);
      } else {
        parts.push(remaining);
        break;
      }
    }
    return parts;
  }

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (line.startsWith("|") && line.endsWith("|")) {
      flushList();
      tableBuffer.push(line);
      continue;
    } else {
      flushTable();
    }

    if (line.match(/^[-*]\s+/)) {
      const item = line.replace(/^[-*]\s+/, "");
      listBuffer.push(item);
      continue;
    } else if (line.match(/^\s*·\s+/)) {
      listBuffer.push(line.replace(/^\s*·\s+/, ""));
      continue;
    } else {
      flushList();
    }

    if (line.startsWith("### ")) {
      elements.push(
        <h3 key={i} style={{ fontSize: "18px", fontWeight: 600, marginTop: "32px", marginBottom: "12px", paddingBottom: "8px", borderBottom: "1px solid #ddd" }}>
          {renderInline(line.slice(4))}
        </h3>
      );
    } else if (line.startsWith("## ")) {
      elements.push(
        <h2 key={i} style={{ fontSize: "20px", fontWeight: 600, marginTop: "32px", marginBottom: "16px" }}>
          {renderInline(line.slice(3))}
        </h2>
      );
    } else if (line.startsWith("# ")) {
      elements.push(
        <h1 key={i} style={{ fontSize: "24px", fontWeight: 700, marginTop: "32px", marginBottom: "16px" }}>
          {renderInline(line.slice(2))}
        </h1>
      );
    } else if (line.trim() === "") {
      elements.push(<div key={i} style={{ height: "8px" }} />);
    } else if (line.trim() === "---") {
      elements.push(<hr key={i} style={{ border: "none", borderTop: "1px solid #eee", margin: "24px 0" }} />);
    } else {
      elements.push(
        <p key={i} style={{ fontSize: "14px", color: "#333", lineHeight: 1.8, margin: "6px 0" }}>
          {renderInline(line)}
        </p>
      );
    }
  }
  flushTable();
  flushList();

  return <div>{elements}</div>;
}
