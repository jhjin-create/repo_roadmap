// 과목 선택 캐스케이드 (활동 유형 → 교과군 → 과목)
// ============================================================
function SubjectCascadePicker({ curriculumData, selectedSubjects, toggleSubject }) {
  // 활동 유형: "creative" (창의적 체험활동) | "subject" (교과 세특)
  const [activityType, setActivityType] = useState("subject");
  const [selectedCategory, setSelectedCategory] = useState("");

  // 교과군 정렬 순서 (데이터 그대로 "~과" 형식 유지)
  const SUBJECT_GROUP_ORDER = [
    "국어과", "수학과", "사회과", "과학과", "영어과",
    "정보과", "음악과", "미술과", "체육과", "실과",
    "교양", "제2외국어"
  ];

  // 창체 과목 (순서 고정)
  const CREATIVE_SUBJECTS = ["자율활동", "동아리활동", "진로활동"];

  // 교과군별로 과목 그룹화 (창의적 체험활동 제외)
  const grouped = useMemo(() => {
    if (!curriculumData) return {};
    const g = {};
    for (const [subject, info] of Object.entries(curriculumData)) {
      const cat = info.교과군 || "기타";
      // 창의적 체험활동·교과(군) 노이즈 제외
      if (cat === "창의적 체험활동" || cat === "교과(군)") continue;
      if (!g[cat]) g[cat] = [];
      g[cat].push({ name: subject, count: info.standards?.length || 0 });
    }
    Object.keys(g).forEach(k => g[k].sort((a, b) => a.name.localeCompare(b.name)));
    return g;
  }, [curriculumData]);

  // 정해진 순서로 정렬
  const categories = useMemo(() => {
    const all = Object.keys(grouped);
    return all.sort((a, b) => {
      const ai = SUBJECT_GROUP_ORDER.indexOf(a);
      const bi = SUBJECT_GROUP_ORDER.indexOf(b);
      if (ai === -1 && bi === -1) return a.localeCompare(b);
      if (ai === -1) return 1;
      if (bi === -1) return -1;
      return ai - bi;
    });
  }, [grouped]);

  // 교과 세특 모드일 때 첫 교과군 자동 선택
  useEffect(() => {
    if (activityType === "subject" && !selectedCategory && categories.length > 0) {
      setSelectedCategory(categories[0]);
    }
  }, [activityType, categories, selectedCategory]);

  if (!curriculumData) {
    return <div style={{ color: "#888", fontSize: "13px" }}>교육과정 데이터 로딩 중...</div>;
  }

  const subjectsInCat = grouped[selectedCategory] || [];
  const otherSelected = selectedSubjects.filter(s => {
    if (activityType === "creative") {
      return !CREATIVE_SUBJECTS.includes(s);
    }
    return !subjectsInCat.some(sub => sub.name === s);
  });

  // 칩 스타일 헬퍼
const chipStyle = (selected) => ({
    padding: "10px 16px",
    border: selected ? "1px solid #3182F6" : "1px solid #E5E8EB", // 선택 시 파란 테두리
    borderRadius: "12px", // 둥글둥글한 사각형
    fontSize: "14px",
    fontWeight: selected ? 600 : 500,
    cursor: "pointer",
    background: selected ? "#E8F3FF" : "white", // 선택 시 산뜻한 연파랑 배경
    color: selected ? "#1B64DA" : "#4E5968", // 텍스트도 파란색으로 통일
    boxShadow: selected ? "0 2px 8px rgba(49, 130, 246, 0.15)" : "0 2px 4px rgba(0,0,0,0.02)", // 선택 시 파란빛 그림자 발광
    transition: "all 0.2s ease-in-out", // 마우스 올릴 때 스르륵 변하는 효과
    userSelect: "none"
  });

  return (
    <div>
      {/* 1단계: 활동 유형 선택 (버튼 2개) */}
      <div style={{ marginBottom: "20px" }}>
        <label style={{ display: "block", fontSize: "12px", fontWeight: 500, color: "#666", marginBottom: "8px" }}>
          1단계. 활동 유형
        </label>
        <div style={{ display: "flex", gap: "8px" }}>
          <button
            onClick={() => { setActivityType("creative"); }}
            style={{
              flex: 1,
              padding: "12px",
              border: activityType === "creative" ? "2px solid #1a1a1a" : "1px solid #ddd",
              borderRadius: "6px",
              background: activityType === "creative" ? "#1a1a1a" : "white",
              color: activityType === "creative" ? "white" : "#1a1a1a",
              fontSize: "14px",
              fontWeight: 500,
              cursor: "pointer",
              transition: "all 0.15s"
            }}
          >
            창의적 체험활동
          </button>
          <button
            onClick={() => { setActivityType("subject"); }}
            style={{
              flex: 1,
              padding: "12px",
              border: activityType === "subject" ? "2px solid #1a1a1a" : "1px solid #ddd",
              borderRadius: "6px",
              background: activityType === "subject" ? "#1a1a1a" : "white",
              color: activityType === "subject" ? "white" : "#1a1a1a",
              fontSize: "14px",
              fontWeight: 500,
              cursor: "pointer",
              transition: "all 0.15s"
            }}
          >
            교과 세특
          </button>
        </div>
      </div>

      {/* 2단계-A: 창의적 체험활동 — 3개 칩 바로 표시 */}
      {activityType === "creative" && (
        <div style={{ marginBottom: "20px" }}>
          <label style={{ display: "block", fontSize: "12px", fontWeight: 500, color: "#666", marginBottom: "8px" }}>
            2단계. 활동 선택
            <span style={{ marginLeft: "8px", color: "#888", fontWeight: 400 }}>
              (성취기준 데이터 없음 — 자유 설계)
            </span>
          </label>
          <div style={{ display: "flex", flexWrap: "wrap", gap: "8px" }}>
            {CREATIVE_SUBJECTS.map(s => (
              <label key={s} style={chipStyle(selectedSubjects.includes(s))}>
                <input
                  type="checkbox"
                  checked={selectedSubjects.includes(s)}
                  onChange={() => toggleSubject(s)}
                  style={{ display: "none" }}
                />
                {s}
              </label>
            ))}
          </div>
        </div>
      )}

      {/* 2단계-B: 교과 세특 — 교과군 드롭다운 + 과목 체크박스 */}
      {activityType === "subject" && (
        <>
          <div style={{ marginBottom: "16px" }}>
            <label style={{ display: "block", fontSize: "12px", fontWeight: 500, color: "#666", marginBottom: "6px" }}>
              2단계. 교과(군) 선택
            </label>
            <select
              value={selectedCategory}
              onChange={(e) => setSelectedCategory(e.target.value)}
              style={{
                width: "100%",
                padding: "10px 12px",
                border: "1px solid #ccc",
                borderRadius: "4px",
                fontSize: "14px",
                fontFamily: "inherit"
              }}
            >
              {categories.map(cat => {
                const subjects = grouped[cat] || [];
                const selectedInCat = subjects.filter(s => selectedSubjects.includes(s.name)).length;
                return (
                  <option key={cat} value={cat}>
                    {cat} ({subjects.length}개{selectedInCat > 0 ? `, ✓${selectedInCat}개 선택됨` : ""})
                  </option>
                );
              })}
            </select>
          </div>

          <div style={{ marginBottom: "20px" }}>
            <label style={{ display: "block", fontSize: "12px", fontWeight: 500, color: "#666", marginBottom: "8px" }}>
              3단계. {selectedCategory} 과목 선택
            </label>
            {(() => {
              // 과목분류 순서 (공통 → 일반 → 진로 → 융합)
              const CLASS_ORDER = ["공통", "일반", "진로", "융합"];
              // subjectsInCat에는 과목분류 정보가 없음. curriculumData에서 가져옴
              const grouped_by_class = {};
              for (const s of subjectsInCat) {
                const cls = (curriculumData[s.name]?.과목분류) || "기타";
                if (!grouped_by_class[cls]) grouped_by_class[cls] = [];
                grouped_by_class[cls].push(s);
              }
              const ordered = Object.keys(grouped_by_class).sort((a, b) => {
                const ai = CLASS_ORDER.indexOf(a);
                const bi = CLASS_ORDER.indexOf(b);
                if (ai === -1 && bi === -1) return a.localeCompare(b);
                if (ai === -1) return 1;
                if (bi === -1) return -1;
                return ai - bi;
              });
              return ordered.map(cls => (
                <div key={cls} style={{ marginBottom: "14px" }}>
                  <div style={{ fontSize: "11px", color: "#888", marginBottom: "6px", letterSpacing: "0.5px", fontWeight: 500 }}>
                    [{cls}] {grouped_by_class[cls].length}개
                  </div>
                  <div style={{ display: "flex", flexWrap: "wrap", gap: "8px" }}>
                    {grouped_by_class[cls].map(s => (
                      <label key={s.name} style={chipStyle(selectedSubjects.includes(s.name))}>
                        <input
                          type="checkbox"
                          checked={selectedSubjects.includes(s.name)}
                          onChange={() => toggleSubject(s.name)}
                          style={{ display: "none" }}
                        />
                        {s.name}
                        {s.count > 0 && (
                          <span style={{ opacity: 0.6, fontSize: "11px", marginLeft: "4px" }}>({s.count})</span>
                        )}
                      </label>
                    ))}
                  </div>
                </div>
              ));
            })()}
          </div>
        </>
      )}

      {/* 다른 카테고리에서 선택한 과목들 표시 */}
      {otherSelected.length > 0 && (
        <div style={{ marginTop: "16px", padding: "12px 14px", background: "#fafaf7", borderRadius: "6px" }}>
          <div style={{ fontSize: "11px", color: "#888", marginBottom: "6px" }}>
            ✓ 다른 곳에서 선택된 과목 ({otherSelected.length}개)
          </div>
          <div style={{ display: "flex", flexWrap: "wrap", gap: "6px" }}>
            {otherSelected.map(s => (
              <span
                key={s}
                onClick={() => toggleSubject(s)}
                style={{
                  padding: "5px 10px",
                  background: "#1a1a1a",
                  color: "white",
                  borderRadius: "16px",
                  fontSize: "12px",
                  cursor: "pointer"
                }}
                title="클릭하여 제거"
              >
                {s} ✕
              </span>
            ))}
          </div>
        </div>
      )}

      {/* 전체 선택 요약 */}
      <div style={{ marginTop: "16px", padding: "10px 14px", background: "#f0f7f4", borderRadius: "6px", fontSize: "13px", color: "#0a7" }}>
        총 {selectedSubjects.length}개 과목 선택됨
        {selectedSubjects.length > 0 && `: ${selectedSubjects.join(", ")}`}
      </div>
    </div>
  );
}

