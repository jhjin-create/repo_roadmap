          const newFiles = images.map((img, idx) => ({
            name: `${fileName} (page ${img.pageNumber}/${numPages})`,
            type: "image",
            base64: img.base64,
            mediaType: img.mediaType,
            fromPdf: true
          }));
          setSchoolContext(prev => ({
            ...prev,
            [area]: { ...prev[area], files: [...prev[area].files, ...newFiles] }
          }));
