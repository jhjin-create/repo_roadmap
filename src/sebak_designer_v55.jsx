// PDF에서 텍스트 추출 + 이미지 PDF 자동 감지
// 반환: { text, isImagePdf, numPages, totalChars }
async function extractPdfText(file) {
  const arrayBuffer = await file.arrayBuffer();
  const pdf = await window.pdfjsLib.getDocument({ data: arrayBuffer }).promise;
  let fullText = "";
  let totalChars = 0;
  for (let i = 1; i <= pdf.numPages; i++) {
    const page = await pdf.getPage(i);
    const pageContent = await page.getTextContent();
    const pageText = pageContent.items.map(item => item.str).join(" ");
    fullText += `\n--- Page ${i} ---\n${pageText}\n`;
    totalChars += pageText.length;
  }
  const avgCharsPerPage = pdf.numPages > 0 ? totalChars / pdf.numPages : 0;
  const isImagePdf = avgCharsPerPage < 100;  // 페이지당 100자 미만이면 이미지 PDF로 판단
  return { text: fullText, isImagePdf, numPages: pdf.numPages, totalChars, pdf };
}

// PDF 페이지를 이미지(base64 PNG)로 렌더링
// scale 1.5 (균형) — 너무 낮으면 텍스트 인식 ↓, 너무 높으면 비용 ↑
async function renderPdfPagesToImages(pdf, scale = 1.5) {
  const images = [];
  for (let i = 1; i <= pdf.numPages; i++) {
    const page = await pdf.getPage(i);
    const viewport = page.getViewport({ scale });
    const canvas = document.createElement("canvas");
    canvas.width = viewport.width;
    canvas.height = viewport.height;
    const ctx = canvas.getContext("2d");
    await page.render({ canvasContext: ctx, viewport }).promise;
    const dataUrl = canvas.toDataURL("image/png");
    const base64 = dataUrl.split(",")[1];
    images.push({
      base64,
      mediaType: "image/png",
      pageNumber: i
    });
  }
  return images;
}

