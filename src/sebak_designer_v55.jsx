// 학과명에서 계열 자동 추출
function determineFaculty(majorName) {
  if (!majorName || typeof majorName !== "string") return null;
  const cleaned = majorName.replace("__custom__", "").trim();
  
  // 우선순위 규칙: "교육"이 포함된 학과는 교육계열로 우선 매칭
  // (예: "수학교육과"는 "수학과"(자연계열)가 아닌 교육계열로)
  if (cleaned.includes("교육") && !cleaned.includes("교육공학과학")) {
    const eduEntry = FACULTY_MAPPING.find(e => e.계열 === "교육계열");
    if (eduEntry) return { 계열: eduEntry.계열, 핵심진로과목: eduEntry.핵심진로과목 };
  }
  
  for (const entry of FACULTY_MAPPING) {
    for (const kw of entry.학과키워드) {
      if (cleaned.includes(kw)) {
        return { 계열: entry.계열, 핵심진로과목: entry.핵심진로과목 };
      }
    }
  }
  return null;  // 매칭 실패
}

