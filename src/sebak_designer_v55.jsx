  async function handleCurriculumUpload(file) {
    if (!file) return;
    setCurriculumLoading(true);
    setCurriculumError("");
    try {
      let text = await file.text();
      // BOM 제거 (Excel 저장 CSV 호환)
      if (text.charCodeAt(0) === 0xFEFF) {
        text = text.slice(1);
      }
      const result = window.Papa.parse(text, {
        header: true,
        skipEmptyLines: true,
        transformHeader: (h) => h.replace(/^\uFEFF/, "").trim()
      });
      if (result.errors && result.errors.length > 0 && result.errors[0].type === "Delimiter") {
        throw new Error("CSV 형식 오류: " + result.errors[0].message);
      }
      const rows = result.data;
      if (rows.length === 0) {
        throw new Error("빈 CSV 파일");
      }
      // 컬럼 검증 (BOM 처리된 헤더 기준)
      const required = ["과목명", "단원명", "성취기준"];
      const firstRow = rows[0];
      const headerKeys = Object.keys(firstRow);
      const missing = required.filter(c => !headerKeys.some(k => k === c));
      if (missing.length > 0) {
        throw new Error("필수 컬럼 누락: " + missing.join(", ") + " | 실제 헤더: " + headerKeys.join(", "));
      }
      // 과목명별 인덱싱
      const indexed = {};
      for (const row of rows) {
        const subject = (row["과목명"] || "").trim();
        if (!subject) continue;
        if (!indexed[subject]) indexed[subject] = {
          교과군: row["교과(군)"] || "",
          과목분류: row["과목분류"] || "",
          standards: []
        };
        // BOM 잔존 가능성 — 다양한 키 시도
        const getCol = (name) => {
          for (const k of Object.keys(row)) {
            if (k.replace(/\uFEFF/g, "").trim() === name) return row[k];
          }
          return "";
        };
        indexed[subject].standards.push({
          코드: getCol("과목코드"),
          단원: getCol("단원명"),
          성취기준: getCol("성취기준"),
          기본키워드: getCol("기본_키워드(교과서 개념)") || getCol("기본_키워드"),
          융합키워드: getCol("융합_키워드(전공 연계)") || getCol("융합_키워드")
        });
      }
      setCurriculumData(indexed);
      const stats = {
        total: rows.length,
        subjects: Object.keys(indexed).length,
        fromCache: false
      };
      setCurriculumStats(stats);

      // storage API에 캐싱 (shared = true: 다른 컨설턴트도 자동 로드)
      const cacheData = JSON.stringify({ indexed, stats });
      const saved = await storageSet(STORAGE_KEYS.CURRICULUM, cacheData);
      if (saved) {
        console.log("교육과정 캐시 저장 완료:", cacheData.length, "자");
      }
    } catch (e) {
      setCurriculumError("CSV 파싱 실패: " + e.message);
    } finally {
      setCurriculumLoading(false);
    }
  }

