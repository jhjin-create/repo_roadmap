// v46: callClaude 함수에 재시도 로직 내장
// 일시적 서버 오류 (429/500/502/503/529) 발생 시 자동 재시도
// 백오프: 5초 → 15초 → 30초 → 60초 (최대 5회 시도)
async function callClaude(systemPrompt, userPrompt, maxTokens = 4096, model = "claude-sonnet-4-20250514", images = [], onRetry = null) {
  // 재시도 백오프 스케줄
  const MAX_RETRIES = 5;
  const backoffSchedule = [5000, 15000, 30000, 60000];
  
  let attempts = 0;
  let lastError = null;
  
  while (attempts < MAX_RETRIES) {
    try {
      // 🔒 [수정] Anthropic 도메인이 아니라 우리가 만든 안전한 백엔드 서버 주소로 요청을 보냅니다!
      // (나중에 웹에 배포할 때는 localhost 대신 진짜 백엔드 웹 주소로 바꾸게 됩니다)
      const response = await fetch("https://roadmapserver.onrender.com/api/chat", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          systemPrompt,
          userPrompt,
          maxTokens,
          model,
          images // [{ base64, mediaType }]
        })
      });
      
      if (!response.ok) {
        const errBody = await response.text();
        if (response.status >= 500 && attempts < MAX_RETRIES - 1) {
          attempts++;
          const waitTime = backoffSchedule[attempts - 1];
          if (typeof onRetry === "function") onRetry(attempts, waitTime, `서버 오류 (${response.status})`);
          await new Promise(r => setTimeout(r, waitTime));
          continue;
        }
        throw new Error(`백엔드 서버 오류: ${response.status} - ${errBody}`);
      }
      
      const data = await response.json();
      return data.text; // 백엔드가 받아온 텍스트 결과 반환
      
    } catch (fetchErr) {
      lastError = fetchErr;
      if (attempts < MAX_RETRIES - 1) {
        attempts++;
        const waitTime = backoffSchedule[attempts - 1];
        if (typeof onRetry === "function") onRetry(attempts, waitTime, "네트워크 오류");
        await new Promise(r => setTimeout(r, waitTime));
        continue;
      }
      throw fetchErr;
    }
  }
  
  throw lastError || new Error("백엔드 통신 실패 (최대 재시도 초과)");
}

